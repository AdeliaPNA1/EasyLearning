        <p>Ilmu Sastra adalah ilmu yang menyelidiki tentang karya sastra secara ilmiah dengan berbagai gejala dan masalah sastra.[1] Sedangkan, Sastra adalah lembaga sosial yang menggunakan bahasa sebagai medium, dan Bahasa itu sendiri merupakan ciptaan sosial.[2] Seorang penelaah sastra harus dapat menerjemahkan pengalaman sastranya dalam bahasa ilmiah, dan harus dapat menjabarkannya dalam uraian yang jelas dan rasional.[3] Dalam Ilmu sastra terdapat beberapa cabang ilmu sastra yang mempermudah jalannya studi sastra.</p>
        <p><b>Cabang ilmu sastra</b> <br> Cabang dalam Ilmu Sastra terbagi menjadi 3 bagian, yaitu: Teori sastra, Sejarah sastra, dan Kritik sastra. <br><br> <b>Teori, Kritik, dan Sejarah Sastra</b><br>Dalam sejarahnya, faktor-faktor sosial memengaruhi evolusi genre-genre sastra, seperti epik ritual, puisi lirik, esai, drama, dan novel.[3] Dalam penjabarannya, teori sastra berarti meneliti hal-hal yang berhubungan dengan kesusastraan dalam suatu karya, misalnya gaya bahasa, jenis sastra, hakikat sastra, aliran-aliran dalam sastra, unsur cerita, dll.[3] Teori sastra juga merupakan studi prinsip, kategori, dan kriteria. Kritik sastra dan sejarah sastra adalah studi karya-karya konkret. Menurut salah satu tokoh, ilmu sastra atau studi sastra selaras dengan filologi. Tokoh tersebut adalah Philiph August Boeckh. Dalam bukunya yang berjudul Encyklopadie und Methodologie der Philologischen Wissenschaften (1887), dia menjabarkan bahwa filologi sebagai knowledge of the known. Beberapa bidang yang termasuk adalah studi sastra, bahasa, seni, politik, agama, dan adat istiadat.

        Filologi Boeckh didasarkan pada kebutuhan mempelajari karya-karya klasik. Dia mempunyai batasan dalam studi sastra, yaitu studi sastra hanya merupakan satu cabang dari filologi, sebagai ilmu kebudayaan secara menyeluruh. Studi sastra mempunyai ciri khas dalam kegiatannya, yaitu membuat interpretasi, meneliti kekhasan suatu karya, dan memberi penilaian. Ada pendapat yang mengatakan bahwa maksud pengarang adalah bahan utama studi sastra, sebenarnya keliru. Makna karya seni tidak sama atau berhenti pada maksud pengarang. Karya sastra berdiri sendiri sebagai suatu sistem nilai.[3]

        Seorang sejarawan sastra tidak akan puas menilai suatu karya sastra dari sudut pandang masa kini saja. Ia akan mengevaluasi masa lalu sesuai dengan kebutuhan gaya dan gerakan sastra masa kini. Mungkin lebih baik lagi, jika sejarawan sastra bisa menyoroti karya sastra dengan sudut pandang zaman ketiga (zaman yang tidak sama dengan pengarang dan kritikus) atau melihat keseluruhan sejarah interpretasi dan kritik pada karya sastra untuk memperoleh makna yang lebih menyeluruh.[3] Dalam kenyataannya, tak ada sejarah yang ditulis tanpa prinsip seleksi dalam usaha memerinci ciri-ciri dan membuat penilaian.[3] Sejarawan sastra yang menolak pentingnya kritik sastra sebetulnya melakukan kritik sastra tanpa disadari.[3] </p><br>

